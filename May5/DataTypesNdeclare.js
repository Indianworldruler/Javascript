// Variable declaration and assignment of different data types

let x = 5; // number
console.log("x (number):", x);

let y = 1e40; // number (scientific notation)
console.log("y (number in scientific notation):", y);

let z = "Hello"; // string
console.log("z (string):", z);

let isTrue = true; // boolean
console.log("isTrue (boolean):", isTrue);

let arr = [1, 2, 3, 4, 5]; // array
console.log("arr (array):", arr);

let obj = { name: "John", age: 30 }; // object
console.log("obj (object):", obj);

let func = function () { return "Hello World"; }; // function
console.log("func (function):", func);
console.log("func() (function call result):", func());

let nullValue = null; // null
console.log("nullValue (null):", nullValue);

let undefinedValue; // undefined
console.log("undefinedValue (undefined):", undefinedValue);

let symbolValue = Symbol("R"); // symbol
console.log("symbolValue (symbol):", symbolValue);

let bigIntValue = BigInt("123456789012345678901234567890"); // BigInt
console.log("bigIntValue (BigInt):", bigIntValue);

let dateValue = new Date(); // Date object (fixed your `.get` error)
console.log("dateValue (Date):", dateValue);

let demo = `the sum of nums is ${3 + 5}`;
console.log("demo (template literal):", demo);



